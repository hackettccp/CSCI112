/**
 *  Demonstrates printing output (with line breaks)
 */

#include <iostream>                                                 //Needed for std::cout and std::endl

/**
 * Main Function. This is where a C++ program begins
 * (Equivalent to the main method in Java programs)
 */
int main() {
    std::cout << "Hello World!" << std::endl;                       //Prints Hello World! with a line break (This is equivalent to: System.out.println("Hello World!"); in Java)
    std::cout << "Hello World!" << std::endl;                       //Prints Hello World! again
    return 0;                                                       //End of main function
}