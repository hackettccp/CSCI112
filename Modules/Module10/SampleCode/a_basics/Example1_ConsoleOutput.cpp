/**
 *  Demonstrates printing output
 */

#include <iostream>                                                 //Needed for std::cout

/**
 * Main Function. This is where a C++ program begins
 * (Equivalent to the main method in Java programs)
 */
int main() {
    std::cout << "Hello World!";                                    //Prints Hello World! (This is equivalent to: System.out.print("Hello World!"); in Java)
    std::cout << "Hello World!";                                    //Prints Hello World! again on the same line of output
    return 0;                                                       //End of main function
}